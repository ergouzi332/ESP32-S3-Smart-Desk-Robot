// 显示心率
void drawHeartSpO2(U8G2_SSD1306_128X64_NONAME_F_HW_I2C &u8g2, int countdownSeconds, bool showFinal, int animatedHeartRate, bool showHeartRate)
{
   /* 绘制WIFI图标 */
   u8g2.drawXBMP(112, 0, 16, 16, wifi_final_bits);

   char buf[32];
   u8g2.enableUTF8Print();
   u8g2.setFont(u8g2_font_wqy16_t_gb2312);

   if (showFinal) {
      // 将最终结果放到屏幕下方的四分之三区域内
      u8g2.drawFrame(4, 16, 120, 44);
      sprintf(buf, "平均心率");
      int x = (128 - u8g2.getUTF8Width(buf)) / 2;
      if (x < 0) x = 0;
      u8g2.drawUTF8(x, 34, buf); // 标题显示在下方区域的上部

      if (showHeartRate) {
         if (animatedHeartRate > 0) {
            sprintf(buf, "%d BPM", animatedHeartRate);
         } else {
            sprintf(buf, "-- BPM");
         }
      } else {
         sprintf(buf, "   ");
      }
      int x2 = (128 - u8g2.getUTF8Width(buf)) / 2;
      if (x2 < 0) x2 = 0;
      u8g2.drawUTF8(x2, 52, buf); // 心率值显示在更下方，位于下方三分之一区域内
      return;
   }

   if (countdownSeconds > 0) {
      sprintf(buf, "倒计时：%ds", countdownSeconds);
      int x = (128 - u8g2.getUTF8Width(buf)) / 2;
      if (x < 0) x = 0;
      u8g2.drawUTF8(x, 54, buf); // 显示在屏幕下方四分之三位置
   } else {
      sprintf(buf, "测量中...");
      int x = (128 - u8g2.getUTF8Width(buf)) / 2;
      if (x < 0) x = 0;
      u8g2.drawUTF8(x, 54, buf); // 放置到下方区域
   }

   if (MAX30102_State.valid)
   {
      sprintf(buf, "心率：%d BPM", MAX30102_State.heartRate);
   }
   else
   {
      sprintf(buf, "心率：-- BPM");
   }
   int x = (128 - u8g2.getUTF8Width(buf)) / 2;
   if (x < 0) x = 0;
   u8g2.drawUTF8(x, 32, buf);
}

